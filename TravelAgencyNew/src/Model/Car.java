package Model;

public class Car {
    private int carId;
    private String Pickup_date;
    private String Return_Date;
    private String Type_car;
    private Double Price;

    public int getCarId() {
        return carId;
    }

    public String getPickup_date() {
        return Pickup_date;
    }

    public String getReturn_Date() {
        return Return_Date;
    }

    public String getType_car() {
        return Type_car;
    }

    public Double getPrice() {
        return Price;
    }
}
